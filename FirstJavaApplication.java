public class FirstJavaApplication{
	public static void main(String[] args)
	{

		/*
		 Difference between println and print
		 */
		System.out.println("Welcome to Java Programming!"); 	// println moves the cursor to the next line
		System.out.print("Hello"); 							// print does not move the cursor to the next line
		System.out.print(" World!");
	}
}