public class EscapeSequences {
	public static void main(String[] args)
	{
		System.out.println("Newline - \\n : \n ...");
		System.out.println("Horizontal tab - \\t : \t ...");
		System.out.println("Carriage return - \\r : \r ...");
		System.out.println("Backslash - \\\\ : \\ ...");
		System.out.println("Double quote - \\\" : \" ...");
		System.out.println("Single quote - \\\' : \' ...");
		System.out.println("Backspace - \\b : \b ...");
		System.out.println("Form Feed - \\f : \f ...");
	}
	
}
